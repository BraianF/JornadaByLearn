horario = 'noite'

if horario == 'dia':
    print("O Sol está quente")
elif horario == 'tarde':
    print("O por do sol está chegando")
elif horario == 'noite':
    print("A lua está linda")
else:
    print("Se não é dia, não é tarde e não é noite, é o quê rapaz???")